        class QuoteManager {
            constructor() {
                this.visibleQuotes = new Set();
                this.currentExportQuote = null;
                this.currentExportCanvas = null;
                this.init();
            }

            init() {
                this.displayQuotes();
                this.setupExportModal();
            }

            createQuoteElement(quote, index) {
                const quoteElement = document.createElement('div');
                quoteElement.className = 'quote';
                quoteElement.dataset.quoteId = index;

                quoteElement.innerHTML = `
                    <div class="quote-content">
                        <div class="quote-text">"${quote.text}"</div>
                        <span class="quote-author">— ${quote.author}</span>
                    </div>
                    <div class="quote-actions">
                        <button class="quote-action-btn export-quote-btn" data-quote-index="${index}">
                            <i class="fas fa-download"></i> Export
                        </button>
                    </div>
                `;

                // Add hover effects
                quoteElement.addEventListener('mouseenter', this.handleQuoteHover.bind(this));
                quoteElement.addEventListener('mouseleave', this.handleQuoteHoverEnd.bind(this));

                return quoteElement;
            }

            handleQuoteHover(e) {
                const quote = e.currentTarget;
                quote.style.transform = 'translateY(-8px) scale(1.02)';
                quote.style.boxShadow = '0 15px 40px rgba(106, 127, 116, 0.2)';
            }

            handleQuoteHoverEnd(e) {
                const quote = e.currentTarget;
                quote.style.transform = 'translateY(0) scale(1)';
                quote.style.boxShadow = '0 4px 15px rgba(106, 127, 116, 0.1)';
            }

            displayQuotes() {
                const quotesContainer = document.querySelector('.quotes-container');
                if (!quotesContainer) return;

                quotesContainer.innerHTML = '';

                contentData.quotes.forEach((quote, index) => {
                    const quoteElement = this.createQuoteElement(quote, index);
                    quotesContainer.appendChild(quoteElement);

                    // Add animation delay based on index
                    quoteElement.style.animationDelay = `${1.8 + (index * 0.2)}s`;

                    // Add click event for export
                    const exportBtn = quoteElement.querySelector('.export-quote-btn');
                    exportBtn.addEventListener('click', (e) => {
                        e.stopPropagation();
                        this.openExportModal(quote);
                    });
                });
            }

            setupExportModal() {
                const exportModal = document.getElementById('exportModal');
                const closeBtn = document.getElementById('closeExportModal');
                const cancelBtn = document.getElementById('cancelExport');
                const exportBtn = document.getElementById('exportAsPng');
                const preview = document.getElementById('exportPreview');

                // Close modal events
                [closeBtn, cancelBtn].forEach(btn => {
                    btn.addEventListener('click', () => {
                        exportModal.classList.remove('active');
                    });
                });

                // Background color selection
                document.querySelectorAll('.export-option').forEach(option => {
                    option.addEventListener('click', () => {
                        document.querySelectorAll('.export-option').forEach(opt => {
                            opt.classList.remove('active');
                        });
                        option.classList.add('active');

                        // Update preview
                        preview.className = 'export-preview';
                        preview.classList.add(option.dataset.bg);
                    });
                });

                // Font selection
                document.querySelectorAll('.font-option').forEach(option => {
                    option.addEventListener('click', () => {
                        document.querySelectorAll('.font-option').forEach(opt => {
                            opt.classList.remove('active');
                        });
                        option.classList.add('active');

                        // Update preview
                        const quoteText = document.getElementById('previewQuoteText');
                        const quoteAuthor = document.getElementById('previewQuoteAuthor');
                        quoteText.className = 'export-quote-text';
                        quoteAuthor.className = 'export-quote-author';
                        quoteText.classList.add(option.dataset.font);
                        quoteAuthor.classList.add(option.dataset.font);
                    });
                });

                // Alignment selection
                document.querySelectorAll('.alignment-option').forEach(option => {
                    option.addEventListener('click', () => {
                        document.querySelectorAll('.alignment-option').forEach(opt => {
                            opt.classList.remove('active');
                        });
                        option.classList.add('active');

                        // Update preview
                        const quoteText = document.getElementById('previewQuoteText');
                        const quoteAuthor = document.getElementById('previewQuoteAuthor');
                        quoteText.className = 'export-quote-text';
                        quoteAuthor.className = 'export-quote-author';
                        quoteText.classList.add(option.dataset.align);
                        quoteAuthor.classList.add(option.dataset.align);
                    });
                });

                // Export as PNG
                exportBtn.addEventListener('click', () => {
                    this.exportQuoteAsPng();
                });

            }

            openExportModal(quote) {
                this.currentExportQuote = quote;
                const exportModal = document.getElementById('exportModal');
                const quoteText = document.getElementById('previewQuoteText');
                const quoteAuthor = document.getElementById('previewQuoteAuthor');
                const hiddenCode = document.getElementById('hiddenCode');

                quoteText.textContent = `"${quote.text}"`;
                quoteAuthor.textContent = `— ${quote.author}`;

                // Generate unique code
                const uniqueCode = this.generateUniqueCode();
                hiddenCode.textContent = `#${uniqueCode}`;

                exportModal.classList.add('active');
            }

            generateUniqueCode() {
                const timestamp = Date.now().toString(36);
                const random = Math.random().toString(36).substring(2, 8);
                return `${timestamp}-${random}`.toUpperCase();
            }

            exportQuoteAsPng() {
                const preview = document.getElementById('exportPreview');

                // Temporarily adjust preview for better export
                const originalWidth = preview.style.width;
                const originalHeight = preview.style.height;
                preview.style.width = '800px';
                preview.style.height = '400px';

                html2canvas(preview, {
                    scale: 3,
                    backgroundColor: null,
                    useCORS: true,
                    logging: false
                }).then(canvas => {
                    // Restore original dimensions
                    preview.style.width = originalWidth;
                    preview.style.height = originalHeight;

                    // Store canvas for sharing
                    this.currentExportCanvas = canvas;

                    // Create download link
                    const link = document.createElement('a');
                    link.download = `quote-${Date.now()}.png`;
                    link.href = canvas.toDataURL('image/png');
                    link.click();

                    // Close modal
                    document.getElementById('exportModal').classList.remove('active');
                });
            }

            shareOnSocialMedia(platform) {
                if (!this.currentExportCanvas) {
                    alert('Please export the quote first before sharing.');
                    return;
                }

                const canvas = this.currentExportCanvas;
                const dataURL = canvas.toDataURL('image/png');

                // For demonstration purposes, we'll just download the image
                // In a real implementation, you would use platform-specific APIs
                const link = document.createElement('a');
                link.download = `quote-to-share-${platform}.png`;
                link.href = dataURL;
                link.click();

                // Show success message
                alert(`Quote prepared for sharing on ${platform}. In a real implementation, this would open the ${platform} sharing dialog.`);
            }
        }

        // Enhanced initialization with error handling and performance optimization
        function init() {
            try {
                // Set current year
                const yearElement = document.getElementById('current-year');
                if (yearElement) {
                    yearElement.textContent = new Date().getFullYear();
                }

                // Initialize quote manager
                const quoteManager = new QuoteManager();

                // Enhanced loading screen handling
                setTimeout(() => {
                    const loadingContainer = document.querySelector('.loading-container');
                    if (loadingContainer) {
                        loadingContainer.style.transition = 'opacity 0.5s ease, transform 0.5s ease';
                        loadingContainer.style.opacity = '0';
                        loadingContainer.style.transform = 'scale(0.95)';

                        setTimeout(() => {
                            loadingContainer.classList.add('hidden');
                        }, 500);
                    }
                }, 1200);

                // Enhanced beforeunload handling
                window.addEventListener('beforeunload', () => {
                    if (document.body) {
                        document.body.style.opacity = '0';
                        document.body.style.transition = 'opacity 0.4s cubic-bezier(0.4, 0, 0.2, 1)';
                    }
                });

            } catch (error) {
                console.error('Initialization error:', error);
                // Fallback: ensure basic functionality works
                const quotesContainer = document.querySelector('.quotes-container');
                if (quotesContainer) {
                    quotesContainer.innerHTML = '<p>Quotes will be available shortly.</p>';
                }
            }
        }

        // Enhanced DOM ready with loading states
        document.addEventListener('DOMContentLoaded', () => {
            // Add loading state to body
            document.body.style.opacity = '0';
            document.body.style.transition = 'opacity 0.6s ease';

            setTimeout(() => {
                init();
                document.body.style.opacity = '1';
            }, 100);
        });